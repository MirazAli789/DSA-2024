#include <bits/stdc++.h>
using namespace std;
int main()
{
     // integers
     int i = 324;
     cout << i << endl;
     cout << sizeof(i) << endl;

     // float
     float f = 23.78;
     cout << f << endl
          << sizeof(f) << endl;

     // charecter
     char c = 'e';
     cout << c << endl
          << sizeof(c) << endl;

     // double
     double d = 23648326.52135;
     cout << d << endl
          << sizeof(d) << endl;

     // long
     long l = 2356123;
     cout << l << endl
          << sizeof(l) << endl;

     // long int
     long int li = 551235;
     cout << li << endl
          << sizeof(li) << endl;

     // boolean
     bool b = false;
     cout << b << endl
          << sizeof(b) << endl;

     // long long
     long long r = 124123416436;
     cout << r << endl
          << sizeof(r) << endl;
     return 0;
}