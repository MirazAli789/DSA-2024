#include<bits/stdc++.h>
using namespace std;
int main()
{
    //if we want to print ' or " inside a string we can use escaped quotation
    string s = "This is a \"good\" line";
    cout<<s<<endl;
    //if we want to print '\' 
    string s1 = "This is a backslash: \\ ";
    cout<<s1;
    cout<<endl;
    return 0;
}
